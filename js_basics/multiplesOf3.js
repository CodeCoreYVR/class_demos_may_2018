// The `for` keyword is another way to do iteration.
// We can do anything with `while` loop that we can
// do with `for` loop.
// In a `for` loop:
// We first declare <variables>.
// A <condition-expr> that when `false` ends the loop.
// A <degrading-expr> that will eventually cause the
// loop to end.

// for (<variables>; <condition-expr>; <degrading-expr>) {
//
// }

for (let i = 0; i <= 100; i += 3) {
  console.log(`Mambo No. ${i}`);
}
